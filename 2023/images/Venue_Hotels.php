<br>
					<em>Public transport</em><br>
<br>
The Helsinki region has an extensive and easy-to-use public transit system operated by the
<a href="https://www.hsl.fi/en" target='_blank' style="color: blue"> <b>Helsinki Region Transport</b></a>, and the congress venue and the recommended hotels are all located close to metro stations, <em>so we recommend all participants utilise the public
transport</em> instead of e.g. renting a car.  <br><br>
As a bonus, <em>early registrars will receive free public transport access</em> for the duration of
the congress. The access code will be sent to participants ahead of time, so you can utilise
the ticket immediately after landing at the airport by installing the HSL app (Android:<a href="https://play.google.com/store/apps/details?id=fi.hsl.app " target='_blank' style="color: blue"> <b>https://play.google.com/store/apps/details?id=fi.hsl.app </b></a>
, IOS: <a href="https://apps.apple.com/fi/app/apple-store/id1340229182?l=fi" target='_blank' style="color: blue"> <b>AppStore </b></a>).<br><br>

From the airport, one can <em>take a train to the centre of Helsinki</em> (the train station is located
right below the airport between terminals 1 and 2). Both the I and P trains travel between the
Airport and the Central Railway Station in Helsinki and take essentially the same time
despite their different routes, so you could try using different trains in each direction to see
different parts of Helsinki on the way.<br><br>
From the Central Railway Station you can then continue to your hotel and event venue with
the metro, which runs every 2,5 minutes during the rush hour. There is a journey planner
available at: <a href="https://www.hsl.fi/en to help you plan your trips" target='_blank' style="color: blue"> <b>https://www.hsl.fi/en</b></a> to help you plan your trips.<br><br>
The congress venue is located next to the metro station <em>Aalto Yliopisto / Aalto University</em>
and <em>exit B</em> is the closest to the venue). Subway stations in the centre of Helsinki include
Kamppi, Rautatieasema/Central railway station, and Helsingin Yliopisto/University of
Helsinki.<br><br>
<li><img src="images/P2.jpg" height="350" width="550"></li><br><br>

For the late registrars, the ticket prices are available here:
<a href="https://www.hsl.fi/en/tickets-and-fares/" target='_blank' style="color: blue"> <b>https://www.hsl.fi/en/tickets-and-fares/</b></a>. <em>Helsinki city centre is located in zone A, the venue is located in Zone B, and the airport is located in Zone C</em>. The price for an AB
ticket is 2,80€ and for an ABC ticket 4,10€. The tickets allow unlimited transfers for the
duration of the ticket, which is 80 minutes for AB and 90 minutes for ABC ticket. (Multi) day
tickets that allow unlimited travel within the zone are also available.<br><br>


